package com.gft.unity.core.pim;

public class DateTime {

    private int year;
    private int month;
    private int day;
    private int hour;
    private int minute;
    private int second;

    public DateTime() {
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getHour() {
        return hour;
    }

    public void setHour(int hours) {
        this.hour = hours;
    }

    public int getMinute() {
        return minute;
    }

    public void setMinute(int minute) {
        this.minute = minute;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    @Override
    public String toString() {
        StringBuilder builder = new StringBuilder();
        builder.append("DateTime [year=");
        builder.append(year);
        builder.append(", month=");
        builder.append(month);
        builder.append(", day=");
        builder.append(day);
        builder.append(", hour=");
        builder.append(hour);
        builder.append(", minute=");
        builder.append(minute);
        builder.append(", second=");
        builder.append(second);
        builder.append("]");
        return builder.toString();
    }
}
